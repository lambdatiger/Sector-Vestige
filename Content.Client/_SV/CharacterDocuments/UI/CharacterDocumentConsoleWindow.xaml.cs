using Content.Client.RichText;
using Content.Shared._SV.CharacterDocuments;
using Content.Shared._SV.CharacterDocuments.Consoles;
using Content.Client._SV.CharacterDocuments.UI;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;
using Robust.Client.State;
using System.Linq;
using Content.Client.Paper.UI;
using Content.Shared.Paper;

namespace Content.Client._SV.CharacterDocuments.UI;

[GenerateTypedNameReferences]
public sealed partial class CharacterDocumentConsoleWindow : DefaultWindow
{
    public CharacterDocumentConsoleWindow()
    {
        RobustXamlLoader.Load(this);
        CrewListing.OnItemSelected += args =>
        {
            if (_isPopulating)
                return;

            if (CrewListing[args.ItemIndex].Metadata is not NetEntity uid)
                return;

            OnPlayerSelected?.Invoke(uid);
        };

        CrewListing.OnItemDeselected += args =>
        {
            if (_isPopulating)
                return;

            DocumentListing.Clear();
            Document.Clear();
            StampDisplay.RemoveStamps();
            StampDisplay.RemoveAllChildren();
        };

        DocumentListing.OnItemSelected += args =>
        {
            if (_isPopulating)
                return;

            if (DocumentListing[args.ItemIndex].Metadata is not int uid)
                return;

            OnDocumentSelected?.Invoke(_selectedPlayer, uid);
        };

        DocumentListing.OnItemDeselected += args =>
        {
            if (_isPopulating)
                return;

            OnDocumentDeselected?.Invoke();
            Document.Clear();
            StampDisplay.RemoveStamps();
            StampDisplay.RemoveAllChildren();
        };

        ScanButton.OnPressed += _ =>
        {
            OnButtonScanPressed?.Invoke(_selectedPlayer, TitleInput.Text);
        };

        DeleteButton.OnPressed += _ =>
        {
            if (_selectedDocument == null) return;
            OnButtonDeletePressed?.Invoke(_selectedPlayer, _selectedDocument);
        };
    }

    public void UpdateState(CharacterDocumentConsoleState state)
    {
        _isPopulating = true;
        CrewListing.Clear();
        DocumentListing.Clear();
        ScanButton.Disabled = !state.SelectedPlayer.HasValue || state.PaperInserted == false;
        PrintButton.Disabled = state.SelectedDocument == null;
        DeleteButton.Disabled = state.SelectedDocument == null;

        foreach (var (uid, name) in state.PlayerList)
        {
            var item = CrewListing.AddItem(name);
            item.Metadata = uid;
        }

        if (state.SelectedPlayer.HasValue)
        {
            _selectedPlayer = state.SelectedPlayer.Value;
            for (var i = 0; i < CrewListing.Count; i++)
            {
                if (CrewListing[i].Metadata is NetEntity uid && uid == state.SelectedPlayer.Value)
                {
                    CrewListing[i].Selected = true;
                    break;
                }
            }
        }

        _isPopulating = false;

        if (state.SelectedPlayerDocuments == null)
        {
            DocumentListingStatus.Visible = false;
            DocumentListing.Visible = true;
        }
        else
        {
            _isPopulating = true;
            DocumentListing.Clear();
            foreach (var doc in state.SelectedPlayerDocuments.Values)
            {
                DocumentListing.AddItem(doc.DocTitle, null, true, doc.DocID);
            }
            DocumentListingStatus.Visible = false;
            DocumentListing.Visible = true;

            if (state.SelectedDocument != null)
            {
                _selectedDocument = state.SelectedDocument;
                for (var i = 0; i < DocumentListing.Count; i++)
                {
                    if (DocumentListing[i].Metadata is int docId && docId == state.SelectedDocument.DocID)
                    {
                        DocumentListing[i].Selected = true;
                        break;
                    }
                }
            }

            _isPopulating = false;
        }

        if (state.SelectedDocument == null)
        {
            _selectedDocument = null;
            Document.Clear();
            TitleInput.Clear();
            StampDisplay.RemoveStamps();
            StampDisplay.RemoveAllChildren();
        }
        else
        {
            _selectedDocument = state.SelectedDocument;
            Document.SetMessage(FormattedMessage.FromMarkupPermissive(state.SelectedDocument.DocContent), UserFormattableTags.BaseAllowedTags);
            TitleInput.Clear();
            TitleInput.SetText(state.SelectedDocument.DocTitle);
            StampDisplay.RemoveStamps();
            StampDisplay.RemoveAllChildren();
            if (state.SelectedDocument.DocStamps.StampedName != null)
                StampDisplay.AddStamp(new StampWidget { StampInfo = state.SelectedDocument.DocStamps });
        }
    }

    private NetEntity _selectedPlayer;
    private CharacterDocument? _selectedDocument;
    public Action<NetEntity>? OnPlayerSelected;
    public Action<NetEntity, int>? OnDocumentSelected;
    public Action<NetEntity, string>? OnButtonScanPressed;
    public Action<NetEntity, CharacterDocument>? OnButtonDeletePressed;
    public Action? OnDocumentDeselected;
    bool _isPopulating;
}
